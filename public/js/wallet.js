/* wallet.js — ∞ Infinity Token Wallet
   Tracks spendable tokens (earned at 1/hour) and a history of minted
   research tokens generated by spinning the Radio Crusher. */

const Wallet = (() => {
  'use strict';

  const KEY       = 'infinityWallet';
  const HOUR_MS   = 3_600_000; // 1 hour in milliseconds
  const MAX_TITLE = 65;        // max chars shown in minted-token list

  // ── State helpers ──────────────────────────────────────────────────────────
  function load() {
    try {
      return Object.assign(
        { spendable: 0, lastGrant: 0, totalEarned: 0, totalSpent: 0, minted: [] },
        JSON.parse(localStorage.getItem(KEY) || '{}'),
      );
    } catch {
      return { spendable: 0, lastGrant: 0, totalEarned: 0, totalSpent: 0, minted: [] };
    }
  }

  function save(w) {
    try { localStorage.setItem(KEY, JSON.stringify(w)); } catch (_) {}
  }

  // ── Token economy ──────────────────────────────────────────────────────────
  /** Grant 1 token per elapsed hour. Returns the number of tokens granted. */
  function grantHourly() {
    const w   = load();
    const now = Date.now();
    if (!w.lastGrant) { w.lastGrant = now; save(w); return 0; }
    const grants = Math.floor((now - w.lastGrant) / HOUR_MS);
    if (grants > 0) {
      w.spendable   += grants;
      w.totalEarned += grants;
      w.lastGrant   += grants * HOUR_MS;
      save(w);
    }
    return grants;
  }

  /** Deduct `amount` tokens. Returns true on success, false if insufficient. */
  function spend(amount) {
    const w = load();
    if (w.spendable < amount) return false;
    w.spendable  -= amount;
    w.totalSpent += amount;
    save(w);
    return true;
  }

  /** Refund tokens (e.g. on failed purchase). */
  function refund(amount) {
    const w = load();
    w.spendable += amount;
    save(w);
  }

  /** Record a newly minted research token (called from app.js on spin). */
  function addMintedToken(tok) {
    const w = load();
    w.minted.unshift({
      spin    : tok.spin,
      title   : tok.title,
      doi     : tok.doi,
      channel : tok.radioChannel || null,
      ts      : Date.now(),
    });
    if (w.minted.length > 50) w.minted = w.minted.slice(0, 50);
    save(w);
  }

  // ── Countdown helpers ──────────────────────────────────────────────────────
  function msUntilNextGrant() {
    const w    = load();
    if (!w.lastGrant) return HOUR_MS;
    return Math.max(0, w.lastGrant + HOUR_MS - Date.now());
  }

  function fmtCountdown(ms) {
    if (ms <= 0) return 'now';
    const m = Math.floor(ms / 60_000);
    const s = Math.floor((ms % 60_000) / 1000);
    return `${m}m ${s}s`;
  }

  // ── DOM refs ───────────────────────────────────────────────────────────────
  const $ = id => document.getElementById(id);

  // ── Render ─────────────────────────────────────────────────────────────────
  function renderWallet() {
    const w = load();

    const set = (id, v) => { const el = $(id); if (el) el.textContent = v; };
    set('wallet-balance',      w.spendable);
    set('wallet-minted-count', w.minted.length);
    set('wallet-total-spent',  w.totalSpent);
    set('header-token-balance', w.spendable);

    const nextEl = $('wallet-next-grant');
    if (nextEl) {
      const ms = msUntilNextGrant();
      nextEl.textContent = ms <= 0 ? 'available now!' : `next in ${fmtCountdown(ms)}`;
    }

    const listEl = $('wallet-tokens-list');
    if (listEl) {
      if (w.minted.length === 0) {
        listEl.innerHTML = '<div class="wallet-empty">No tokens minted yet. Spin the reels!</div>';
      } else {
        listEl.innerHTML = w.minted.map(t => {
          const ch = t.channel ? `<span class="wallet-token-channel">${t.channel}</span>` : '';
          return `<div class="wallet-token-item">
            <span class="wallet-token-spin">#${t.spin}</span>
            ${ch}
            <span class="wallet-token-title">${esc(t.title).substring(0, MAX_TITLE)}…</span>
            <span class="wallet-token-doi">${esc(t.doi)}</span>
          </div>`;
        }).join('');
      }
    }
  }

  function esc(s) {
    return String(s)
      .replace(/&/g,'&amp;').replace(/</g,'&lt;')
      .replace(/>/g,'&gt;').replace(/"/g,'&quot;');
  }

  // ── Init ───────────────────────────────────────────────────────────────────
  function initWallet() {
    grantHourly();
    renderWallet();

    // Tick every 10 s to keep countdown fresh and grant any accrued tokens
    setInterval(() => {
      const granted = grantHourly();
      if (granted > 0) {
        renderWallet();
        if (typeof Marketplace !== 'undefined') Marketplace.renderBalance();
      } else {
        // Just refresh countdown + header badge
        const w   = load();
        const ms  = msUntilNextGrant();
        const el  = $('wallet-next-grant');
        const hdr = $('header-token-balance');
        if (el)  el.textContent  = ms <= 0 ? 'available now!' : `next in ${fmtCountdown(ms)}`;
        if (hdr) hdr.textContent = w.spendable;
      }
    }, 10_000);
  }

  return { load, save, spend, refund, addMintedToken, grantHourly, renderWallet, initWallet };
})();
